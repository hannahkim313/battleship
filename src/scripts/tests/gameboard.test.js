test('check if all ships are sunk', () => {
  const gameboard = Gameboard();
  const ship1 = Ship(2);
  gameboard.placeShip(ship1, ['1', 'a']);
  gameboard.receiveAttack(['1', 'a']);
  gameboard.receiveAttack(['1', 'b']);
  expect(gameboard.isAllSunk()).toEqual(true);
});

